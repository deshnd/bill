func readIntegers() -> [Int] {
    var integers = [Int]()
 // Read integers from standard input until end-of-file is reached
    while let input = readLine(), let integer = Int(input) {
        integers.append(integer)   
    }
    return integers
}
func printIntegers(integers: [Int]) {
    // Print the contents of the array, one element per line
    for integer in integers {
        print(integer)       
    }
}
