// Function to read lines of text from Standard Input until end-of-file is reached
func readLines() -> [String] {
      var lines = [String]()
        while let input = readLine() {
        lines.append(input)    
    }
        return lines
    }
// Function to print the contents of an array, one element per line
func printLines(lines: [String]) {
  for line in lines {
           print(line)      
 }  
}

